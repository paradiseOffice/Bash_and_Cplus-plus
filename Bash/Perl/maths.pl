#!/usr/bin/perl

$number_one = 5;
$number_two = 3;

print "Adding numbers: \n";
$result = $number_one + $number_two;
print "5+3 = $result \n\n";

print "subtracting numbers: \n";
$result = $number_one - $number_two;
print "5 - 3 = $result \n\n";

print "multiplying numbers: \n";
$result = $number_one * $number_two;
print "5 x 3 = $result \n\n";

print "Dividing numbers: \n";
$result = $number_one / $number_two;
print "5 / 3 = $result \n\n";

print "Raising numbers to a power: \n";
$result = $number_one ** $number_two;
print "5 raised to the power of 3 = $result \n\n";

print "Obtaining a remainder after a division: \n";
$result = $number_one % $number_two;
print "5 modulo 3 = $result \n\n";

print "Bye, have a nice day!";